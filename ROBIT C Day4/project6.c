#include <stdio.h>

int main()
{
    int arr[12];
    int num;

    /*
    도형에 들어가는 숫자는 최대 12개이다.
    새로운 숫자를 입력할 때마다 배열의 다음 위치에 저장한다.
    */
    for (int i = 0; i < 12; i++) {

        if (i == 0) {
            printf("input: ");
        }
        else {
            printf("next input: ");
        }

        scanf("%d", &num);

        /* 새로 입력한 숫자를 배열에 저장 */
        arr[i] = num;

        /*
        도형을 출력할 때마다 첫 번째 숫자부터 시작해야 하므로
        k를 0으로 초기화한다.
        */
        int k = 0;

        /*
        총 12개의 숫자를 출력하여
        가로 4칸 세로 3칸의 도형을 만든다.
        */
        for (int j = 0; j < 12; j++) {

            /*
            arr[k]와 같은 의미이다.
            포인터 연산을 사용하여 현재 위치의 값을 출력한다.
            */
            printf("%d", *(arr + k));

            /*
            다음 입력 숫자로 이동한다.
            마지막 입력 숫자까지 출력했다면
            다시 첫 번째 숫자로 돌아간다.
            */
            k++;

            if (k > i) {
                k = 0;
            }

            /* 숫자 4개를 출력할 때마다 다음 줄로 이동 */
            if (j % 4 == 3) {
                printf("\n");
            }
        }

        printf("\n");
    }

    return 0;
}